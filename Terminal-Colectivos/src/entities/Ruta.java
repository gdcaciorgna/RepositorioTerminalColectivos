package entities;

public class Ruta {
	
	private int cod_ruta;
	private String dias_sem;
	
	
	public int getCod_ruta() {
		return cod_ruta;
	}
	public void setCod_ruta(int cod_ruta) {
		this.cod_ruta = cod_ruta;
	}
	public String getDias_sem() {
		return dias_sem;
	}
	public void setDias_sem(String dias_sem) {
		this.dias_sem = dias_sem;
	}
	
	

}
