package entities;

public class Empresa_Colectivo {
	
	private int id_empresa_colectivo;
	private String nombre;
	
	
	
	public int getId_empresa_colectivo() {
		return id_empresa_colectivo;
	}
	public void setId_empresa_colectivo(int id_empresa_colectivo) {
		this.id_empresa_colectivo = id_empresa_colectivo;
	}
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
